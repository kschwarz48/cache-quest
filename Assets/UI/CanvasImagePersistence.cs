using UnityEngine;

public class CanvasImagePersistence : MonoBehaviour
{
    public static CanvasImagePersistence Instance { get; private set; }

    void Awake()
    {
        if (Instance == null)
        {
            Instance = this;
            DontDestroyOnLoad(gameObject);
        }
        else if (Instance != this)
        {
            Destroy(gameObject);
        }
    }
}
